package operatorler;

public class AritmetikOperatorler {
    public static void main(String[] args){
        int sayi1=60;
        int sayi2=40;

        System.out.println("Sayi1 : " +sayi1 + " Sayi2 : "+sayi2 + " Toplam Degeri : " + (sayi1+sayi2));
        System.out.println("Sayi1 : " +sayi1 + " Sayi2 : "+sayi2 + " Farklari : " + (sayi1-sayi2));
        System.out.println("Sayi1 : " +sayi1 + " Sayi2 : "+sayi2 + " Bolumleri : " + ((double)sayi1/(double)sayi2));
        System.out.println("Sati1 : " +sayi1 + " Sayi2 : "+sayi2 + " Carpimlari : " + (sayi1*sayi2));
        System.out.println("Sati1 : " +sayi1 + " Sayi2 : "+sayi2 + " Modu : " + (sayi1%sayi2));



    }
}
